/**
*  根据图片url获取网络图片尺寸
*/
+ (CGSize)getImageSizeWithURL:(id)URL{
    NSURL * url = nil;
    if ([URL isKindOfClass:[NSURL class]]) {
        url = URL;
    }
    if ([URL isKindOfClass:[NSString class]]) {
        url = [NSURL URLWithString:URL];
    }
    if (!URL) {
        return CGSizeZero;
    }
    CGImageSourceRef imageSourceRef = CGImageSourceCreateWithURL((CFURLRef)url, NULL);
    CGFloat width = 0, height = 0;
    
    if (imageSourceRef) {
        
        // 获取图像属性
        CFDictionaryRef imageProperties = CGImageSourceCopyPropertiesAtIndex(imageSourceRef, 0, NULL);
        
        //以下是对手机32位、64位的处理
        if (imageProperties != NULL) {
            
            CFNumberRef widthNumberRef = CFDictionaryGetValue(imageProperties, kCGImagePropertyPixelWidth);
            
#if defined(__LP64__) && __LP64__
            if (widthNumberRef != NULL) {
                CFNumberGetValue(widthNumberRef, kCFNumberFloat64Type, &width);
            }
            
            CFNumberRef heightNumberRef = CFDictionaryGetValue(imageProperties, kCGImagePropertyPixelHeight);
            
            if (heightNumberRef != NULL) {
                CFNumberGetValue(heightNumberRef, kCFNumberFloat64Type, &height);
            }
#else
            if (widthNumberRef != NULL) {
                CFNumberGetValue(widthNumberRef, kCFNumberFloat32Type, &width);
            }
            
            CFNumberRef heightNumberRef = CFDictionaryGetValue(imageProperties, kCGImagePropertyPixelHeight);
            
            if (heightNumberRef != NULL) {
                CFNumberGetValue(heightNumberRef, kCFNumberFloat32Type, &height);
            }
#endif
            /***************** 此处解决返回图片宽高相反问题 *****************/
            // 图像旋转的方向属性
            NSInteger orientation = [(__bridge NSNumber *)CFDictionaryGetValue(imageProperties, kCGImagePropertyOrientation) integerValue];
            CGFloat temp = 0;
            switch (orientation) {  // 如果图像的方向不是正的，则宽高互换
                case UIImageOrientationLeft: // 向左逆时针旋转90度
                case UIImageOrientationRight: // 向右顺时针旋转90度
                case UIImageOrientationLeftMirrored: // 在水平翻转之后向左逆时针旋转90度
                case UIImageOrientationRightMirrored: { // 在水平翻转之后向右顺时针旋转90度
                    temp = width;
                    width = height;
                    height = temp;
                }
                    break;
                default:
                    break;
            }
            /***************** 此处解决返回图片宽高相反问题 *****************/

            CFRelease(imageProperties);
        }
        CFRelease(imageSourceRef);
    }
    return CGSizeMake(width, height);
}

// 高斯模糊
+ (UIImage *)blurryImageGaussian:(UIImage *)image   blurLevel:(CGFloat)blur {
    CIContext *context = [CIContext contextWithOptions:nil];
        CIImage *inputImage= [CIImage imageWithCGImage:image.CGImage];
        //设置filter
        CIFilter *filter = [CIFilter filterWithName:@"CIGaussianBlur"];
        [filter setValue:inputImage forKey:kCIInputImageKey]; [filter setValue:@(blur) forKey: @"inputRadius"];
        //模糊图片
        CIImage *result=[filter valueForKey:kCIOutputImageKey];
        CGImageRef outImage=[context createCGImage:result fromRect:[result extent]];
        UIImage *blurImage=[UIImage imageWithCGImage:outImage];
        CGImageRelease(outImage);
        return blurImage;
}

// 添加通用模糊效果 // image是图片，blur是模糊度
+ (UIImage *)blurryImage:(UIImage *)image withBlurLevel:(CGFloat)blur {
    if ( !image) {
        return nil ;
    }
    //    NSInteger boxSize = (NSInteger)(10 * 5);
       NSData *imageData = UIImageJPEGRepresentation(image, 1); // convert to jpeg
       UIImage* destImage = [UIImage imageWithData:imageData];
       
       if (blur < 0.f || blur > 1.f) {
           blur = 0.5f;
       }
       int boxSize = (int)(blur * 40);
       boxSize = boxSize - (boxSize % 2) + 1;
       
       CGImageRef img = destImage.CGImage;
       vImage_Buffer inBuffer, outBuffer;
       vImage_Error error;
       void *pixelBuffer;
       
       //create vImage_Buffer with data from CGImageRef
       CGDataProviderRef inProvider = CGImageGetDataProvider(img);
       CFDataRef inBitmapData = CGDataProviderCopyData(inProvider);
       
       inBuffer.width = CGImageGetWidth(img);
       inBuffer.height = CGImageGetHeight(img);
       inBuffer.rowBytes = CGImageGetBytesPerRow(img);
       inBuffer.data = (void*)CFDataGetBytePtr(inBitmapData);
       
       //create vImage_Buffer for output
       pixelBuffer = malloc(CGImageGetBytesPerRow(img) * CGImageGetHeight(img));
       
       if(pixelBuffer == NULL)
       
       outBuffer.data = pixelBuffer;
       outBuffer.width = CGImageGetWidth(img);
       outBuffer.height = CGImageGetHeight(img);
       outBuffer.rowBytes = CGImageGetBytesPerRow(img);
       
       // Create a third buffer for intermediate processing
       void *pixelBuffer2 = malloc(CGImageGetBytesPerRow(img) * CGImageGetHeight(img));
       vImage_Buffer outBuffer2;
       outBuffer2.data = pixelBuffer2;
       outBuffer2.width = CGImageGetWidth(img);
       outBuffer2.height = CGImageGetHeight(img);
       outBuffer2.rowBytes = CGImageGetBytesPerRow(img);
       
       //perform convolution
       error = vImageBoxConvolve_ARGB8888(&inBuffer, &outBuffer2, NULL, 0, 0, boxSize, boxSize, NULL, kvImageEdgeExtend);
       if (error) {
           
       }
       error = vImageBoxConvolve_ARGB8888(&outBuffer2, &inBuffer, NULL, 0, 0, boxSize, boxSize, NULL, kvImageEdgeExtend);
       if (error) {
           
       }
       error = vImageBoxConvolve_ARGB8888(&inBuffer, &outBuffer, NULL, 0, 0, boxSize, boxSize, NULL, kvImageEdgeExtend);
       if (error) {
           NSLog(@"error from convolution %ld", error);
       }
       
       CGColorSpaceRef colorSpace = CGColorSpaceCreateDeviceRGB();
       CGContextRef ctx = CGBitmapContextCreate(outBuffer.data, outBuffer.width , outBuffer.height , 8, outBuffer.rowBytes, colorSpace, (CGBitmapInfo)kCGImageAlphaNoneSkipLast);
       CGImageRef imageRef = CGBitmapContextCreateImage (ctx);
       UIImage *returnImage = [UIImage imageWithCGImage:imageRef];
       
       //clean up
       CGContextRelease(ctx);
       CGColorSpaceRelease(colorSpace);
       
       free(pixelBuffer);
       free(pixelBuffer2);
       CFRelease(inBitmapData);
       
       CGImageRelease(imageRef);
       
       return returnImage;
}

